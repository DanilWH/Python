current_number = 1
while current_number <= 5:
	print(current_number)
	current_number += 1
# В данном случае, цикл while продолжает работать пока значение current_number меньше или равно 5.
# Оператор += является сокращённой формой записи для current_number = current_number + 1.
# "Цикл while вв дейтсявии" стр.124-125.
