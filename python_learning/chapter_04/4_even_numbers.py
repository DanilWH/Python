even_numbers = list(range(2,11,2))
print(even_numbers)
# Функция range() также может генерировать числовые последовательности, пропуская числа в заданном диапозоне. В примере выше показано построение списка чётных чисел от 1 до 10.
# В верхнем примере вункция range() начинает со значения 2, а затем увеличиввает его на 2. Приращение 2 последовательно применяется до тех пор, пока не будет достигнуто или пройдено конечное значение 11, после чего выводится результат.
