age = 17
if age >= 18:
	print('You are old enough to vote!')# Вы достаточно взрослые, чтобы голосовать!
	print('Have you registered to vote yet?')#Вы уже зарегистрировались для голосования?
else:
	print('Sorry, you are too young to vote.')# Извините, вы слишком молоды, чтобы голосовать.
	print('Please register to vote as you turn 18!')# Пожалуйста, зарегистрируйтесь, чтобы проголосовать, когда вам исполнится 18 лет!

# Если условие истино, то выполняется блок if с командами print. Если же услоыие ложно, выполняется блок else.
